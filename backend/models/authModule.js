const {Schema, model} = require("mongoose")

const registerSchema = new Schema({
    userName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    image: {
        type: String,
        required: true
    },
},{timestamp: true})

module.exports = model("user", registerSchema)